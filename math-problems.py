#     return x * y

def mul_bits(x, y, bites):
    x &= (2 ** bites - 1)
    y &= (2 ** bites - 1)
    return x * y

def mul16(x, y):
    x_hi = x >> 8
    x_lo = x & 0xFF
    
    y_hi = y >> 8
    y_lo = y & 0xFF

    p1 = mul_bits(x_hi, y_hi, 8)
    p2 = mul_bits(x_hi, y_lo, 8)
    p3 = mul_bits(x_lo, y_hi, 8)
    p4 = mul_bits(x_lo, y_lo, 8)

    result = (p1 << 16) + ((p2 + p3) << 8) + p4
    
    return result

def run_tests():
    print("Запуск тестов для mul16...")

    assert mul16(10, 20) == 200, "Ошибка в тесте 10 * 20"
    assert mul16(255, 255) == 65025, "Ошибка в тесте 255 * 255"
    assert mul16(256, 2) == 512, "Ошибка в тесте 256 * 2"
    max_val = (1 << 16) - 1
    assert mul16(max_val, max_val) == max_val * max_val, "Ошибка в тесте макс. значений"
    val_a = 0x1234 # 4660
    val_b = 0x5678 # 22136
    assert mul16(val_a, val_b) == val_a * val_b, f"Ошибка в тесте {val_a} * {val_b}"

    print("Все тесты для mul16 пройдены успешно!")

run_tests()

