# Реализуйте аналогичную функцию fast_pow для возведения в степень.
# Решение необходимо получить только с помощью небольших модификаций предыдущего решения.

def fast_pow(x, n):
    r = 1
    while n:
        if n & 1:
            r *= x
        n >>= 1
        x *= x
    return r

def run_tests():
    print("Запуск тестов для fast_pow...")
    
    assert fast_pow(2, 10) == 1024, "Ошибка: 2^10 != 1024"
    
    assert fast_pow(5, 0) == 1, "Ошибка: 5^0 != 1"
    
    assert fast_pow(7, 1) == 7, "Ошибка: 7^1 != 7"
    
    assert fast_pow(6, 2) == 36, "Ошибка: 6^2 != 36"
    
    assert fast_pow(3, 4) == 81, "Ошибка: 3^4 != 81"

    print("Все тесты для fast_pow пройдены успешно!")

run_tests()