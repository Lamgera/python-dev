# Предположим, что стоит задача перемножить два 16-битных числа без знака.
# Однако в наличии у нас имеется только операция умножения 8-битных чисел. 
# Побитовые сдвиги умножениями не считаются.
# Выведите формулу для умножения «половинок» и 
# реализуйте функцию mul16(x, y), используя приведенную ниже функцию mul_bits,
# которая имитирует умножение с ограниченной числом bits разрядностью аргументов.

# def mul_bits(x, y, bits):
#     x &= (2 ** bits - 1)
